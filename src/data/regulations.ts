// 根據缺失類型取得相關法規
export function getRegulationsByCategory(category: string): RegulationArticle[] {
  const results: RegulationArticle[] = [];
  
  for (const cat of ALL_REGULATIONS) {
    for (const article of cat.articles) {
      if (article.category.includes(category) || 
          (article.title && article.title.includes(category)) ||
          article.content.includes(category)) {
        results.push(article);
      }
    }
  }
  
  return results;
}

// 根據關鍵字搜尋法規
export function searchRegulations(keyword: string): RegulationArticle[] {
  const results: RegulationArticle[] = [];
  
  for (const cat of ALL_REGULATIONS) {
    for (const article of cat.articles) {
      if (article.article.includes(keyword) || 
          (article.title && article.title.includes(keyword)) ||
          article.content.includes(keyword)) {
        results.push(article);
      }
    }
  }
  
  return results;
}

// 取得特定條文
export function getRegulation(articleNumber: string): RegulationArticle | undefined {
  for (const cat of ALL_REGULATIONS) {
    const article = cat.articles.find(a => a.article === articleNumber || a.article === `第${articleNumber}條`);
    if (article) return article;
  }
  return undefined;
}
